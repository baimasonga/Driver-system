import '../models/models.dart';

/// Converts between Supabase's snake_case row shape and the camelCase JSON
/// shape the model classes' fromJson/toJson already understand, so the
/// model files themselves stay backend-agnostic.

Map<String, dynamic> vehicleRowToJson(Map<String, dynamic> r) => {
      'id': r['id'],
      'registrationNumber': r['registration_number'],
      'make': r['make'],
      'model': r['model'],
      'year': r['year'],
      'type': r['type'],
      'fuelType': r['fuel_type'],
      'tankCapacity': r['tank_capacity'],
      'expectedFuelConsumption': r['expected_fuel_consumption'],
      'currentOdometer': r['current_odometer'],
      'status': r['status'],
      'assignedDriverId': r['assigned_driver_id'] ?? '',
      'assignedDepartment': r['assigned_department'],
      'insuranceExpiry': r['insurance_expiry'],
      'roadworthinessExpiry': r['roadworthiness_expiry'],
      'gpsTrackerId': r['gps_tracker_id'],
      'trackerStatus': r['tracker_status'],
      'lastGpsLocation': {
        'lat': r['last_gps_lat'] ?? 0,
        'lng': r['last_gps_lng'] ?? 0,
        'address': r['last_gps_address'] ?? '',
      },
      'lastGpsUpdateTime': r['last_gps_update_time'] ?? '',
      'monthlyFuelLimit': r['monthly_fuel_limit'],
      'currentMonthFuelUsed': r['current_month_fuel_used'],
    };

Map<String, dynamic> vehicleToRow(Vehicle v) => {
      'id': v.id,
      'registration_number': v.registrationNumber,
      'make': v.make,
      'model': v.model,
      'year': v.year,
      'type': v.type,
      'fuel_type': v.fuelType,
      'tank_capacity': v.tankCapacity,
      'expected_fuel_consumption': v.expectedFuelConsumption,
      'current_odometer': v.currentOdometer,
      'status': v.status.label,
      'assigned_driver_id': v.assignedDriverId.isEmpty ? null : v.assignedDriverId,
      'assigned_department': v.assignedDepartment,
      'insurance_expiry': v.insuranceExpiry,
      'roadworthiness_expiry': v.roadworthinessExpiry,
      'gps_tracker_id': v.gpsTrackerId,
      'tracker_status': v.trackerStatus.label,
      'last_gps_lat': v.lastGpsLocation.lat,
      'last_gps_lng': v.lastGpsLocation.lng,
      'last_gps_address': v.lastGpsLocation.address,
      'last_gps_update_time': v.lastGpsUpdateTime,
      'monthly_fuel_limit': v.monthlyFuelLimit,
      'current_month_fuel_used': v.currentMonthFuelUsed,
    };

